package com.entity;


public class station { 
	int sid;
	String sname;
	String sdate;
	String sfunction;
	int swages;
	int did;
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getSdate() {
		return sdate;
	}
	public void setSdate(String sdate) {
		this.sdate = sdate;
	}
	public String getSfunction() {
		return sfunction;
	}
	public void setSfunction(String sfunction) {
		this.sfunction = sfunction;
	}
	public int getSwages() {
		return swages;
	}
	public void setSwages(int swages) {
		this.swages = swages;
	}
	public int getDid() {
		return did;
	}
	public void setDid(int did) {
		this.did = did;
	}
}
